import re

class DeviceClass:

    def __init__(self):

        self.Unidirectional = 'False'
        self.connectionCounter = 5
        self.DefaultResponseTimeout = 0.3
        self._compile_list = {}
        self.Subscription = {}
        self.ReceiveData = self.__ReceiveData
        self._ReceiveBuffer = b''
        self.counter = 0
        self.connectionFlag = True
        self.initializationChk = True
        self.Debug = False
        self.Models = {}

        self.Commands = {
            #'ConnectionStatus': {'Status': {}},
            'ActivePopup': {'Status': {}},
            'CameraSpeed': {'Parameters': ['Camera Number'], 'Status': {}},
            'KeyOnPreview': {'Parameters': ['Keyer'], 'Status': {}}
        }


    def SetActivePopup(self, value, qualifier):
        self.WriteStatus('ActivePopup', value)

    def SetCameraSpeed(self, value, qualifier):
        self.WriteStatus('CameraSpeed', value, qualifier)

    def SetKeyOnPreview(self, value, qualifier):
        self.WriteStatus('KeyOnPreview', value, qualifier)


###################################################################

    def __SetHelper(self, command, commandstring, value, qualifier):
        self.Debug = True
        
        self.Send(commandstring)


    def __UpdateHelper(self, command, commandstring, value, qualifier):

        if self.Unidirectional == 'True':
            print('Inappropriate Command ', command)
        else:
            if self.initializationChk:
                self.OnConnected()
                self.initializationChk = False

            self.counter = self.counter + 1
            if self.counter > self.connectionCounter and self.connectionFlag:
                self.OnDisconnected()

            self.Send(commandstring)


    def OnConnected(self):
        self.connectionFlag = True
        self.WriteStatus('ConnectionStatus', 'Connected')
        self.counter = 0

    def OnDisconnected(self):
        self.WriteStatus('ConnectionStatus', 'Disconnected')
        self.connectionFlag = False
        self.HandshakeCompleted = False

